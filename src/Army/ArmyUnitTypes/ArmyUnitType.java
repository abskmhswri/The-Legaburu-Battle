package Army.ArmyUnitTypes;

public class ArmyUnitType {
    private int strength = 0;
    private int power = 1;

    public int getStrength()
    {
        return strength;
    }

    public int getPower()
    {
        return power;
    }

    public void setStrength(int iStrength){
        strength = iStrength;
    }

    public void setPower(int iPower){
        power = iPower;
    }
}
